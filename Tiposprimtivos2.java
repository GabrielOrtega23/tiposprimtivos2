public class Tiposprimtivos2 {

    public static void main(String[] args) {
     // Declaração de novas variáveis primitivas com contexto do Brasil
        byte numeroFilhos = 2; // Tipo byte para o número médio de filhos por família no Brasil
        short temperatura = 30; // Tipo short para uma temperatura comum em várias regiões do Brasil
        int distanciaViagem = 3500; // Tipo int para a distância de uma viagem típica no Brasil, como de São Paulo a Porto Alegre
        long populacaoBrasil = 213993437L; // Tipo long para a população do Brasil (aproximadamente 213 milhões, dado de 2023)
        float mediaNotas = 7.2f; // Tipo float para a média de notas no Brasil
        double salarioMensal = 2600.50; // Tipo double para o salário médio mensal no Brasil (aproximadamente em 2023)
        char grauInstrução = 'S'; // Tipo char para o grau de instrução (S para superior, comum em muitos casos)
        boolean casado = true; // Tipo boolean para saber se a pessoa é casada (vamos supor que sim)

        // Exibição dos valores
        System.out.println("Número de filhos: " + numeroFilhos);
        System.out.println("Temperatura: " + temperatura + "°C");
        System.out.println("Distância da viagem: " + distanciaViagem + " km");
        System.out.println("População do Brasil: " + populacaoBrasil);
        System.out.println("Média de notas: " + mediaNotas);
        System.out.println("Salário mensal: R$ " + salarioMensal);
        System.out.println("Grau de instrução: " + grauInstrução);
        System.out.println("Está casado? " + casado);
    }
}
    

